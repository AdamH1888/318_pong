/* Small delay for OLED power-up */
static inline void oledDelay(void)
{
    for (volatile uint32_t i = 0; i < 200000; i++)
    {
        __NOP();
    }
}

/* -------------------------------------------------------------------------- */
/*  Low-level I2C send                                                         */
/* -------------------------------------------------------------------------- */
void sendOLED(uint8_t *buffer, uint16_t size, uint8_t CD)
{
    LPI2C_MasterStart(I2C_OLED, OLED_ADDRESS, kLPI2C_Write);
    LPI2C_MasterSend(I2C_OLED, &CD, 1);
    LPI2C_MasterSend(I2C_OLED, buffer, size);
    LPI2C_MasterStop(I2C_OLED);
}

/* -------------------------------------------------------------------------- */
/*  Addressing                                                                */
/* -------------------------------------------------------------------------- */
void setPage(uint8_t page)
{
    uint8_t pageCommand = 0xB0U + page;
    sendOLED(&pageCommand, 1, OLED_COMMAND);
}

void setSeg(uint8_t seg)
{
    /* Apply SH1106 offset HERE ONLY */
    uint8_t col = seg + OLED_X_OFFSET;

    uint8_t segCommand[2] =
    {
        (uint8_t)(0x00U | (col & 0x0F)),
        (uint8_t)(0x10U | ((col & 0xF0) >> 4))
    };

    sendOLED(segCommand, 2, OLED_COMMAND);
}

/* -------------------------------------------------------------------------- */
/*  Clear screen                                                              */
/* -------------------------------------------------------------------------- */
void fillOLED(uint8_t data)
{
    uint8_t line[SH1106_RAM_WIDTH];
    memset(line, data, sizeof(line));

    for (uint8_t page = 0; page < 8; page++)
    {
        setPage(page);
        setSeg(0);
        sendOLED(line, sizeof(line), OLED_DATA);
    }
}

void fillPage(uint8_t data)
{
    uint8_t pageBuf[128];
    memset(pageBuf, data, sizeof(pageBuf));

    setSeg(0);
    sendOLED(pageBuf, sizeof(pageBuf), OLED_DATA);
}

/* -------------------------------------------------------------------------- */
/*  Scroll                                                                    */
/* -------------------------------------------------------------------------- */
void scrollOLED(uint8_t rows)
{
    uint8_t cmd[2] = {0xD3, rows};
    sendOLED(cmd, 2, OLED_COMMAND);
}

/* -------------------------------------------------------------------------- */
/*  Init                                                                      */
/* -------------------------------------------------------------------------- */
void initOLED(void)
{
    uint8_t init[] =
    {
        0xAE,
        0xD5, 0x80,
        0xA8, 0x3F,
        0xD3, 0x00,
        0x40,
        0x20, 0x02,   /* PAGE addressing mode */
        0xA1,
        0xC8,
        0xDA, 0x12,
        0x81, 0xCF,
        0xD9, 0xF1,
        0xDB, 0x30,
        0x2E,
        0xA4,
        0xA6,
        0xAF
    };

    oledDelay();

    sendOLED(init, sizeof(init), OLED_COMMAND);

    fillOLED(0x00);

    currCharLoc[0] = 0;
    currCharLoc[1] = 0;

    setPage(0);
    setSeg(0);
}

/* -------------------------------------------------------------------------- */
/*  Text                                                                      */
/* -------------------------------------------------------------------------- */
void writeChar(uint8_t character, bool inverted)
{
    (void)inverted;
    sendOLED((uint8_t *)&font[character][0], 6, OLED_DATA);
}

void lineWrap(void)
{
    if (currCharLoc[0] >= 21)
    {
        currCharLoc[0] = 0;
        currCharLoc[1]++;

        setPage(currCharLoc[1] % 8);
        fillPage(0x00);
        setSeg(0);
    }
}

